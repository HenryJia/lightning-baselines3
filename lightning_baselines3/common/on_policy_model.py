import time
from typing import Any, Callable, Dict, List, Optional, Tuple, Type, Union

import gym
import numpy as np

import torch
import pytorch_lightning as pl

from lightning_baselines3.common import logger
from lightning_baselines3.common.base_class import BaseAlgorithm
from lightning_baselines3.common.buffers import RolloutBuffer
